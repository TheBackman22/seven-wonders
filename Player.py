class Player:
    def _init_(name, wonder, hand, board, might, resources, coins, player_left, player_right, player_across):
        self.name = name
        self.wonder = wonder
        self.hand = hand
        self.board = board
        self.might = might
        self.resources = resources
        self.coins = coins
        self.player_left = player_left
        self.player_right = player_right
        self.player_across = player_across
